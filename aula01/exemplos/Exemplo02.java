package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        // variaveis: TIPO nome;
        int valor = 15;

        valor = valor + 3 * 2;
        System.out.println("valor = " + (valor));

        valor = 15;

        valor = (valor + 3) * 2;
        System.out.println("valor = " + (valor));
    }
}
